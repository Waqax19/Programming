public class TwoDArrays
{
    public static void main(String[] args)
    {
        int[][] elements = {
                {5,4,3,2,1},
                {4,8,9,5,7,5,4,2},
                {3,2,7,9,5}
        };

        System.out.println("Length of the array is : " + elements.length);

        System.out.println("Size of First row is : " + elements[0].length);

        System.out.println("Size of second row is : " + elements[1].length);

        System.out.println("Size of third row is : " + elements[2].length);


    }
}
